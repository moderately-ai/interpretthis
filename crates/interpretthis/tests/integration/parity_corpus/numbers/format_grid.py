# Numeric format-spec corners: sign, fill/align, grouping, precision, bases.
print(f"{1234567:,}", f"{1234567:_}")
print(f"{-42:+}", f"{42: }", f"{42:+}")
print(f"{255:#x}", f"{255:#X}", f"{8:#o}", f"{5:#b}")
print(f"{3.14159:.2f}", f"{3.14159:10.2f}", f"{3.14159:<10.2f}|")
print(f"{0.5:.0%}", f"{1234.5:e}", f"{1234.5:.2e}")
print(f"{42:*^10}", f"{42:*<10}", f"{42:*>10}")
print(f"{-5:05}", f"{5:05}", f"{-5:+06}")
print(f"{3.0:g}", f"{0.0001:g}", f"{1000000.0:g}")
print("{:,.2f}".format(1234567.891))
print(f"{255:08b}")
