import json
data = {"name": "test", "values": [1, 2, 3], "nested": {"a": True, "b": None}}
s = json.dumps(data)
print(s)
print(json.dumps(data, sort_keys=True))
print(json.dumps(data, indent=2))
parsed = json.loads(s)
print(parsed["name"], parsed["values"], parsed["nested"]["a"])
print(json.dumps([1, "two", 3.0, True, None, [4, 5]]))
print(json.dumps({"key": "value with \"quotes\""}))
print(json.dumps({"unicode": "café ☕"}))
print(json.dumps({"unicode": "café"}, ensure_ascii=False))
print(json.loads('{"a": 1, "b": [2, 3], "c": {"d": 4}}'))
print(json.loads('[1, 2.5, "three", true, false, null]'))
print(json.dumps({"a": 1, "b": 2}, separators=(",", ":")))
print(json.dumps(3.14159))
print(json.dumps({"nested": {"deep": {"value": [1, 2, {"x": "y"}]}}}))
print(json.loads('"just a string"'))
print(json.loads("42"), json.loads("3.14"), json.loads("true"))
print(json.dumps({"int": 42, "float": 3.14, "bool": True}))
print(json.dumps("string with\nnewline"))
print(json.dumps({}) , json.dumps([]))
print(json.dumps({"a": [1, {"b": [2, {"c": 3}]}]}))
d = json.loads('{"list": [1, 2, 3], "dict": {"nested": true}}')
print(d["list"][1], d["dict"]["nested"])
print(json.dumps({"key": None}))
print(json.dumps(2**53))
print(json.dumps({"tab": "a\tb", "slash": "a/b"}))
print(json.dumps([{"id": i, "sq": i*i} for i in range(3)]))
big = {"users": [{"name": f"user{i}", "active": i % 2 == 0} for i in range(3)]}
print(json.dumps(big, sort_keys=True))
print(json.loads(json.dumps({"roundtrip": [1, 2, {"three": 3}]})))
print(json.dumps(float("1e100")))
print(json.dumps({"a": 1}, indent=4))
