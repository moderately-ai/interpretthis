import base64
print(base64.b64encode(b"Hello, World!"))
print(base64.b64decode(b"SGVsbG8sIFdvcmxkIQ=="))
print(base64.b64encode(b"foo").decode())
print(base64.b64decode("Zm9v"))
print(base64.urlsafe_b64encode(b"\xfb\xff"))
print(base64.urlsafe_b64decode(b"-_8="))
print(base64.b16encode(b"ABC"))
print(base64.b16decode(b"414243"))
print(base64.b32encode(b"foo"))
print(base64.b32decode(b"MZXW6==="))
print(base64.b64encode(b"").decode())
print(base64.standard_b64encode(b"test"))
print(base64.b64encode(bytes([0, 1, 2, 253, 254, 255])))
