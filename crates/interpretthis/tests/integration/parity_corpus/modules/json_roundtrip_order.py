import json
print(json.dumps({"a": 1, "b": [1, 2, 3], "c": None, "d": True}))
print(json.dumps([1, 2.5, "x", False, None]))
print(json.loads('{"key": "value", "num": 42, "arr": [1, 2]}'))
print(json.dumps({"nested": {"x": [1, {"y": 2}]}}))
print(json.dumps("hello\nworld\t!"))
print(json.dumps({"z": 1, "a": 2}, sort_keys=True))
print(json.dumps([1, 2], separators=(",", ":")))
print(json.dumps({"a": 1}, indent=2))
print(json.loads("[true, false, null, 1.5]"))
print(json.dumps(3.14), json.dumps(-0.0))
print(json.loads('"\\u00e9"'))
d = json.loads('{"a": [1, 2, {"b": 3}]}'); print(d["a"][2]["b"])
