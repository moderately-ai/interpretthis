from datetime import datetime, date, time, timedelta, timezone
d = date(2024, 3, 15)
print(d.year, d.month, d.day)
print(d.weekday(), d.isoweekday())
print(d.isoformat())
print(d.strftime("%Y-%m-%d"))
print(d.strftime("%A, %B %d, %Y"))
print(d + timedelta(days=10))
print(d - date(2024, 1, 1))
dt = datetime(2024, 3, 15, 14, 30, 45)
print(dt.hour, dt.minute, dt.second)
print(dt.isoformat())
print(dt.strftime("%Y-%m-%d %H:%M:%S"))
print(dt.date(), dt.time())
print(dt + timedelta(hours=2, minutes=15))
delta = timedelta(days=5, hours=3, minutes=30)
print(delta.days, delta.seconds)
print(delta.total_seconds())
print(timedelta(hours=25))
print(timedelta(weeks=2))
t = time(9, 30, 0)
print(t.hour, t.minute)
print(t.isoformat())
print(t.strftime("%H:%M"))
print(date(2024, 12, 31) - date(2024, 1, 1))
d2 = datetime(2024, 1, 1, 12, 0, 0, tzinfo=timezone.utc)
print(d2.isoformat())
print(d2.tzname())
print(datetime(2024, 6, 15).strftime("%j"))
print(date(2024, 2, 29).isoformat())
print((date(2025, 1, 1) - date(2024, 1, 1)).days)
print(datetime(2024, 3, 15, 14, 30).strftime("%p"))
print(date(2024, 3, 15).strftime("%w"))
print(sorted([date(2024, 3, 1), date(2024, 1, 1), date(2024, 2, 1)]))
print(date(2024, 1, 1) < date(2024, 12, 31))
dt1 = datetime(2024, 1, 1, 0, 0, 0)
dt2 = datetime(2024, 1, 2, 12, 30, 0)
print((dt2 - dt1))
print(timedelta(days=1) * 3)
print(timedelta(hours=10) / timedelta(hours=2))
print(timedelta(days=1) + timedelta(hours=12))
print(d.replace(year=2025))
print(dt.replace(hour=0, minute=0))
