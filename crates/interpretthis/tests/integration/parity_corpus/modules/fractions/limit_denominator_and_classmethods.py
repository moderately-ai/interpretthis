from fractions import Fraction as F
from decimal import Decimal as D
print(F(3, 4).limit_denominator(2))
print(F(3.14159).limit_denominator(100))
print(F.from_float(0.5))
print(F.from_decimal(D("1.25")))
print(F.from_decimal(D("0.1")))
print(F(22, 7).limit_denominator(10))
print(F(1, 3) + F(1, 6), F(2, 4))
print(F(10, 4), F(-6, 8))
print(F(1, 2) ** 2)
print(F(3, 4).numerator, F(3, 4).denominator)
print(float(F(1, 3)), int(F(7, 2)))
print(F(1, 2) < F(2, 3), F(3, 4) == F(6, 8))
print(F(5, 10).limit_denominator())
print(F("3/7"), F("0.25"))
print(F(1, 2).as_integer_ratio())
print(round(F(7, 3)), round(F(7, 3), 2))
print(F(10, 3) // F(1, 3), F(10, 3) % F(1, 2))
print(abs(F(-3, 4)), -F(1, 2))
print(F(1, 3).__floor__(), F(7, 3).__ceil__())
print(F(3, 4) + 1, 1 + F(3, 4), F(1, 2) * 4)
print(max(F(1, 2), F(2, 3), F(3, 5)))
