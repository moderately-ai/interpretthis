import json
print(json.dumps({"list": [1, 2, 3], "nested": {"a": 1}}))
print(json.loads('{"escaped": "line1\\nline2"}'))
print(json.dumps("unicode: é"))
print(json.dumps(3.14))
print(json.dumps([True, False, None]))
print(json.loads('[1, 2.5, "three", true, null]'))
print(json.dumps({"key with spaces": 1}))
print(json.dumps({}, indent=2))
print(json.dumps([1, [2, [3, [4]]]]))
data = json.loads('{"a": {"b": {"c": 42}}}')
print(data["a"]["b"]["c"])
print(json.dumps({"tab": "a\tb"}))
print(json.dumps(-0.0))
print(json.dumps(1e100))
print(json.loads('"\\u00e9"'))
