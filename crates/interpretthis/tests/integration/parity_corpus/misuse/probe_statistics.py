import statistics
print(statistics.mean([1, 2, 3, 4, 5]))
print(statistics.median([1, 2, 3, 4]))
print(statistics.median([1, 2, 3, 4, 5]))
print(statistics.mode([1, 1, 2, 3]))
print(statistics.stdev([1, 2, 3, 4, 5]))
print(round(statistics.variance([1, 2, 3, 4, 5]), 4))
print(statistics.median_low([1, 2, 3, 4]))
print(statistics.median_high([1, 2, 3, 4]))
print(statistics.harmonic_mean([1, 2, 4]))
print(statistics.fmean([1, 2, 3, 4]))
print(statistics.pvariance([1, 2, 3, 4, 5]))
print(round(statistics.pstdev([1, 2, 3, 4, 5]), 4))
print(statistics.multimode([1, 1, 2, 2, 3]))
print(statistics.quantiles([1, 2, 3, 4, 5], n=4))
