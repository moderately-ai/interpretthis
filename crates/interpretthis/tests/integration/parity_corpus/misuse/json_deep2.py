import json
print(json.dumps({"b": 2, "a": 1}, sort_keys=True))
print(json.dumps([1, 2, 3], indent=2))
print(json.dumps({"key": "value"}, indent=4))
print(json.loads('{"a": [1, 2, {"b": true}]}'))
print(json.dumps({"n": None, "t": True, "f": False}))
print(json.dumps("hello \"world\""))
print(json.loads('[1.5, 2.5, -3]'))
print(json.dumps({"unicode": "café"}))
print(json.dumps({"unicode": "café"}, ensure_ascii=True))
print(json.dumps([], indent=2))
print(json.dumps({"nested": {"deep": [1, {"x": 2}]}}))
data = json.loads('{"count": 42, "items": ["a", "b"]}')
print(data["count"], data["items"][0])
