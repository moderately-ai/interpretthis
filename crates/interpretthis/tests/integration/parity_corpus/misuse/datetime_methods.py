from datetime import datetime, date, timedelta
d = date(2024, 6, 15)
print(d.weekday())
print(d.isoweekday())
print(d.isoformat())
print(d.strftime("%A, %B %d"))
print(d.replace(year=2025))
print(d.toordinal() > 0)
dt = datetime(2024, 6, 15, 14, 30, 45)
print(dt.date())
print(dt.time())
print(dt.strftime("%Y-%m-%dT%H:%M:%S"))
print(dt.replace(hour=10))
print((dt + timedelta(days=1)).day)
print(date(2024, 1, 1).isoweekday())
print(datetime(2024, 12, 25).strftime("%j"))
print(date.fromisoformat("2024-03-14"))
print(date(2024, 2, 29).year)
