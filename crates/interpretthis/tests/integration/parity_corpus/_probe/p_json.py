import json
print(json.dumps({"a": 1, "b": [2, 3], "c": None, "d": True}))
print(json.dumps([1, 2.5, "text", False]))
print(json.loads('{"x": 1, "y": [2, 3]}'))
print(json.dumps({"nested": {"deep": [1, {"k": "v"}]}}))
print(json.dumps("special: \n\t\" chars"))
print(json.loads('[1, 2.5, "a", null, true, false]'))
print(json.dumps({"unicode": "café"}))
print(json.dumps({"unicode": "café"}, ensure_ascii=False))
print(json.dumps([1, 2, 3], indent=2))
print(json.dumps({"b": 2, "a": 1}, sort_keys=True))
print(json.loads('{"num": 3.14, "int": 42, "neg": -5}'))
print(json.dumps({"key": "value"}, separators=(",", ":")))
print(json.dumps(float("inf")))
print(json.loads('1.5e10'))
print(json.dumps({1: "int-key"}))
print(type(json.loads("42")).__name__, type(json.loads("4.2")).__name__)
