# Pins: title, swapcase, zfill, center, ljust, rjust, casefold,
# encode, expandtabs — common formatting / normalisation methods.
print("Hello WORLD".title())
print("Hello WORLD".swapcase())
print("123".zfill(6))
print("hi".center(10, "*"))
print("hi".ljust(6, "."))
print("hi".rjust(6, "."))
print("Café".casefold())
print("abc".encode("utf-8"))
print("Hello\tWorld".expandtabs(4))
